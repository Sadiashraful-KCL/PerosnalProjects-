public class Crop{

  private String type;
  private int value;

  public Crop(String type, int value){
    this.type = type;
    this.value = value;
  }

  public void setType(String type){
    this.type = type;
  }

  public void setValue(int value){
    this.value = value;
  }

  public int getValue(){
    return this.value;
  }



















}
